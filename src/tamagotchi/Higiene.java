    private void jLabel4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel4MouseClicked
        try {
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT i.bolsas, t.caca FROM Inventario i, Tamagotchi t WHERE i.mascota_id = t.id AND t.id = 1");

            if (rs.next()) {
                int bolsas = rs.getInt("bolsas");
                boolean tieneCaca = rs.getBoolean("caca");

                if (bolsas > 0 && tieneCaca) {
                    String sql = "UPDATE Inventario SET bolsas = bolsas - 1 WHERE mascota_id = 1";
                    stmt.executeUpdate(sql);

                    sql = "UPDATE Tamagotchi SET caca = 0 WHERE id = 1";
                    stmt.executeUpdate(sql);

                    rs.close();
                    stmt.close();
                    obtenerValores(conn);
                }
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jLabel4MouseClicked

    private void jLabel6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel6MouseClicked
        try {
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT jabon, manchado FROM Inventario i, Tamagotchi t WHERE i.mascota_id = t.id AND t.id = 1");

            if (rs.next()) {
                int jabon = rs.getInt("jabon");
                boolean estaManchado = rs.getBoolean("manchado");

                if (jabon > 0 && estaManchado) {
                    String sql = "UPDATE Inventario SET jabon = jabon - 1 WHERE mascota_id = 1";
                    stmt.executeUpdate(sql);

                    sql = "UPDATE Tamagotchi SET manchado = 0 WHERE id = 1";
                    stmt.executeUpdate(sql);

                    rs.close();
                    stmt.close();
                    obtenerValores(conn);
                }
            }
            rs.close();
            stmt.close();
            obtenerValores(conn);
        } catch (SQLException ex) {
            ex.printStackTrace();
        }

    }//GEN-LAST:event_jLabel6MouseClicked

