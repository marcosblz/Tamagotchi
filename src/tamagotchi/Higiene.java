    public void obtenerValores(Connection conn) throws SQLException {
    Statement stmt = conn.createStatement();
    ResultSet rs = stmt.executeQuery("SELECT jabon, bolsas FROM Inventario WHERE mascota_id = 1");

    if (rs.next()) {
        int bolsas = rs.getInt("bolsas");
        int jabon = rs.getInt("jabon");
        
        jLabel24.setText("<html><div style='text-align: center;'>Existencias: "+bolsas+"</div></html>");
        jLabel25.setText("<html><div style='text-align: center;'>Existencias: "+jabon+"</div></html>");
        }
    rs.close();
    stmt.close();
    }
    
    
    

